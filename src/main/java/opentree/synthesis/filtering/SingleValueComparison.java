package opentree.synthesis.filtering;

/**
 * Comparison methods that are defined for single values. Used when creating tests to be applied during filtering.
 * @author cody
 *
 */
public enum SingleValueComparison {

	IS_EQUAL_TO,
	IS_UNEQUAL_TO,
	IS_GREATER_THAN,
	IS_LESS_THAN,
	IS_GREATER_THAN_OR_EQUAL_TO,
	IS_LESS_THAN_OR_EQUAL_TO
	
}
